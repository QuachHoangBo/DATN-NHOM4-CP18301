const mongoose = require("mongoose");

const loginSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Xuất mô hình
const Login = mongoose.model("Login", loginSchema);

module.exports = Login;
